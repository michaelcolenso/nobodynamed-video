def build(out_path: Path) -> None:
    out_path.parent.mkdir(parents=True, exist_ok=True)
    if out_path.exists():
        out_path.unlink()

    conn = sqlite3.connect(str(out_path))
    conn.execute(
        """
        CREATE TABLE names (
            name  TEXT    NOT NULL,
            sex   TEXT    NOT NULL CHECK(sex IN ('M','F')),
            year  INTEGER NOT NULL,
            count INTEGER NOT NULL,
            PRIMARY KEY (name, sex, year)
        )
        """
    )
    for name, sex, series in FIXTURE_DATA:
        conn.executemany(
            "INSERT INTO names (name, sex, year, count) VALUES (?, ?, ?, ?)",
            [(name, sex, yr, cnt) for yr, cnt in series],
        )
    conn.commit()
    conn.close()
    print(f"✓ wrote {out_path}  ({sum(len(s) for _, _, s in FIXTURE_DATA)} rows)")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Build SSA fixture SQLite database.")
    parser.add_argument(
        "--out",
        type=Path,
        default=Path("fixtures/ssa.sqlite"),
        help="Output path (default: fixtures/ssa.sqlite)",
    )
    args = parser.parse_args()
    build(args.out)
